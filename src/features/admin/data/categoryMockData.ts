// Helper function to get categories with children
export function getCategoriesWithChildren(): CategoryWithChildren[] {
  const mainCategories = mockCategories.filter(cat => cat.parentId === null);
  
  return mainCategories.map(mainCat => ({
    ...mainCat,
    children: mockCategories.filter(cat => cat.parentId === mainCat.id)
  }));
}

// Helper function to get category hierarchy
export function getCategoryHierarchy(categoryId: string): Category[] {
  const category = mockCategories.find(cat => cat.id === categoryId);
  if (!category) return [];
  
  const hierarchy = [category];
  
  if (category.parentId) {
    const parent = mockCategories.find(cat => cat.id === category.parentId);
    if (parent) {
      hierarchy.unshift(parent);
    }
  }
  
  return hierarchy;
}